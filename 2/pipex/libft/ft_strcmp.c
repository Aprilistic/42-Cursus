int	ft_strcmp(const char *s1, const char *s2)
{
	int	idx;

	idx = 0;
	while (1)
	{
		if (s1[idx] != s2[idx])
			return (s1[idx] - s2[idx]);
		if (s1[idx] == 0 && s2[idx] == 0)
			return (0);
		idx++;
	}
}
